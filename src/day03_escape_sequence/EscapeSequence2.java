package day03_escape_sequence;

public class EscapeSequence2 {
    public static void main(String[] args){
        System.out.println("\"iphone case\",\"iphone\",\"xbox\",\"webcam\",\"monitor\"");
        System.out.println("\"iphone case\"\n\"iphone\"\n\"xbox\"\n\"webcam\"\n\"monitor\"");
        System.out.println("\"iphone case\"\t\"iphone\"\t\"xbox\"\t\"webcam\"\t\"monitor\"");
        System.out.println("   ^   ");
        System.out.println(" /   \\");
        System.out.println("|     |");
        System.out.println(" -----");
    }
}
