package day03_escape_sequence;

public class EscapeSequence {
    public static void main(String[] args){
        System.out.println("I like\"java\"programming");
        System.out.println("I like drinking \"coffee\" and \"tea\".");
        System.out.println("\"");
        System.out.println('\\');
        System.out.println("java\nintellj");
        System.out.println("\tjava\tsellenium\tapi\t");
    }
}
